        '세부 북서쪽의 열대 낙원으로, 보라카이에 버금가는 백사장과 청록색 바다를 자랑합니다. 버진 아일랜드, 오그통 동굴 등 다양한 관광지와 저렴한 해산물 요리로 유명합니다.',
  },
};

void main() {
  print('=' * 60);
  print('📝 Travel Spots texts 업데이트 도구');
  print('=' * 60);
  print('');

  // JSON 파일 읽기
  final jsonFile = File(jsonFilePath);
  if (!jsonFile.existsSync()) {
    print('❌ 오류: $jsonFilePath 파일을 찾을 수 없습니다.');
    exit(1);
  }

  final jsonString = jsonFile.readAsStringSync();
  final List<dynamic> travelSpots = jsonDecode(jsonString);

  print('📋 총 여행지 수: ${travelSpots.length}개');
  print('📝 업데이트 대상: ${updatesData.length}개');
  print('');

  int updatedCount = 0;

  // 업데이트 실행
  for (final entry in updatesData.entries) {
    final index = entry.key;
    final updateData = entry.value;

    if (index >= travelSpots.length) {
      print('⚠️  인덱스 $index가 범위를 벗어났습니다. 건너뜁니다.');
      continue;
    }

    final spot = travelSpots[index];
    final name = spot['name'] ?? '(이름 없음)';

    print('🔄 [$index] $name 업데이트 중...');

    // texts 업데이트
    if (updateData.containsKey('texts')) {
      spot['texts'] = updateData['texts'];
      print(
          '   ✅ texts 업데이트 완료 (${(updateData['texts'] as List).length}개 항목)');
    }

    // description 업데이트
    if (updateData.containsKey('description')) {
      spot['description'] = updateData['description'];
      print('   ✅ description 업데이트 완료');
    }

    // title 업데이트
    if (updateData.containsKey('title')) {
      spot['title'] = updateData['title'];
      print('   ✅ title 업데이트 완료');
    }

    updatedCount++;
  }

  // JSON 파일 저장
  final encoder = const JsonEncoder.withIndent('    ');
  jsonFile.writeAsStringSync(encoder.convert(travelSpots));

  print('');
  print('=' * 60);
  print('✅ 업데이트 완료!');
  print('   업데이트된 항목: $updatedCount개');
  print('=' * 60);
}
