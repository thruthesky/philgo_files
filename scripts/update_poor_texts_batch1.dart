    },
  };

  // 업데이트 실행
  int updateCount = 0;
  for (var entry in updatesData.entries) {
    final index = entry.key;
    final data = entry.value;

    if (index < spots.length) {
      // texts 업데이트
      if (data.containsKey('texts')) {
        spots[index]['texts'] = data['texts'];
      }
      // description 업데이트
      if (data.containsKey('description')) {
        spots[index]['description'] = data['description'];
      }
      updateCount++;
      print('✅ [$index] ${spots[index]['name']} 업데이트 완료');
    } else {
      print('❌ [$index] 인덱스 범위 초과');
    }
  }

  // JSON 파일 저장
  final encoder = const JsonEncoder.withIndent('    ');
  await jsonFile.writeAsString(encoder.convert(spots));

  print('\n======================================');
  print('📊 업데이트 결과');
  print('======================================');
  print('총 업데이트: $updateCount개 항목');
  print('저장 완료: lib/philgo_files/travel/travel_spots.json');
}
